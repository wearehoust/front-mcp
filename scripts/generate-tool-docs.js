// Auto-generate docs/TOOL_REFERENCE.md from source files.
// Usage: node scripts/generate-tool-docs.js
// No external dependencies — uses only node:fs and node:path.

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const POLICY_FILE = path.join(ROOT, "src", "policy", "default-policy.ts");
const TOOLS_DIR = path.join(ROOT, "src", "tools");
const OUT_DIR = path.join(ROOT, "docs");
const OUT_FILE = path.join(OUT_DIR, "TOOL_REFERENCE.md");

const TIER_EMOJI = {
  read: "\u{1F535}",        // blue circle
  write: "\u{1F7E1}",       // yellow circle
  destructive: "\u{1F534}", // red circle
};

const TIER_LABEL = {
  read: "Read",
  write: "Write",
  destructive: "Destructive",
};

const TIER_DECISION = {
  read: "Allow",
  write: "Confirm",
  destructive: "Deny",
};

// ---------------------------------------------------------------------------
// 1. Parse ACTION_TIERS from the policy file
// ---------------------------------------------------------------------------

function parseActionTiers() {
  const src = fs.readFileSync(POLICY_FILE, "utf-8");

  // Grab the object literal assigned to ACTION_TIERS (everything between the
  // first { after the declaration and its matching }).
  const start = src.indexOf("const ACTION_TIERS");
  if (start === -1) {
    throw new Error("ACTION_TIERS not found in " + POLICY_FILE);
  }

  // Find the opening brace of the top-level object
  const firstBrace = src.indexOf("{", start);
  let depth = 0;
  let end = -1;
  for (let i = firstBrace; i < src.length; i++) {
    if (src[i] === "{") depth++;
    if (src[i] === "}") depth--;
    if (depth === 0) {
      end = i + 1;
      break;
    }
  }
  if (end === -1) {
    throw new Error("Could not find closing brace for ACTION_TIERS");
  }

  const raw = src.slice(firstBrace, end);

  // Convert TS-style object to valid JSON:
  //   - strip single-line comments
  //   - wrap unquoted keys in double-quotes
  //   - replace trailing commas before } or ]
  let json = raw
    .replace(/\/\/.*$/gm, "")                        // remove // comments
    .replace(/(\w+)\s*:/g, '"$1":')                   // quote keys
    .replace(/:\s*"([^"]+)"/g, ': "$1"')              // keep string values
    .replace(/,\s*([\]}])/g, "$1");                   // strip trailing commas

  try {
    return JSON.parse(json);
  } catch (e) {
    throw new Error("Failed to parse ACTION_TIERS as JSON: " + e.message);
  }
}

// ---------------------------------------------------------------------------
// 2. Extract DESCRIPTION from each tool file
// ---------------------------------------------------------------------------

function extractDescriptions() {
  const descriptions = {};
  const files = fs.readdirSync(TOOLS_DIR).filter((f) => f.endsWith(".tool.ts"));

  for (const file of files) {
    const filePath = path.join(TOOLS_DIR, file);
    const src = fs.readFileSync(filePath, "utf-8");

    // Match TOOL_NAME
    const nameMatch = src.match(/const\s+TOOL_NAME\s*=\s*"([^"]+)"/);
    if (!nameMatch) continue;
    const toolName = nameMatch[1];

    // Match DESCRIPTION — may span multiple lines via string concatenation
    // Patterns:
    //   const DESCRIPTION = "...";
    //   const DESCRIPTION =\n  "..." + "...";   (template literal unlikely)
    const descMatch = src.match(
      /const\s+DESCRIPTION\s*=\s*\n?\s*"((?:[^"\\]|\\.)*)"\s*;/,
    );
    if (descMatch) {
      descriptions[toolName] = descMatch[1].replace(/\\"/g, '"');
    } else {
      // Try multiline concatenation: const DESCRIPTION =\n  "part1"\n  "part2";
      const multiMatch = src.match(
        /const\s+DESCRIPTION\s*=\s*\n?\s*((?:"(?:[^"\\]|\\.)*"\s*\+?\s*\n?\s*)+);/,
      );
      if (multiMatch) {
        const parts = multiMatch[1].match(/"((?:[^"\\]|\\.)*)"/g);
        if (parts) {
          descriptions[toolName] = parts
            .map((p) => p.slice(1, -1).replace(/\\"/g, '"'))
            .join("");
        }
      }
    }
  }

  return descriptions;
}

// ---------------------------------------------------------------------------
// 3. Generate the markdown
// ---------------------------------------------------------------------------

function generate(tiers, descriptions) {
  const toolNames = Object.keys(tiers).sort();
  let totalActions = 0;

  const lines = [];

  lines.push("<!-- This file is auto-generated by scripts/generate-tool-docs.js — do not edit manually. -->");
  lines.push("");
  lines.push("# Tool Reference");
  lines.push("");
  lines.push("> **Auto-generated** — run `npm run generate-docs` to rebuild.");
  lines.push("");
  lines.push("## Policy Tier Legend");
  lines.push("");
  lines.push("| Emoji | Tier | Default Decision |");
  lines.push("|-------|------|------------------|");
  for (const tier of ["read", "write", "destructive"]) {
    lines.push(
      `| ${TIER_EMOJI[tier]} | ${TIER_LABEL[tier]} | ${TIER_DECISION[tier]} |`,
    );
  }
  lines.push("");

  for (const tool of toolNames) {
    const actions = tiers[tool];
    const actionNames = Object.keys(actions);
    totalActions += actionNames.length;

    const desc = descriptions[tool] || "_No description available._";

    lines.push(`## \`${tool}\``);
    lines.push("");
    lines.push(desc);
    lines.push("");
    lines.push("| Action | Tier |");
    lines.push("|--------|------|");
    for (const action of actionNames) {
      const tier = actions[action];
      lines.push(
        `| \`${action}\` | ${TIER_EMOJI[tier]} ${TIER_LABEL[tier]} |`,
      );
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push(
    `**${toolNames.length} tools, ${totalActions} actions** across all tiers.`,
  );
  lines.push("");

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  process.stderr.write("Parsing ACTION_TIERS...\n");
  const tiers = parseActionTiers();

  process.stderr.write("Extracting tool descriptions...\n");
  const descriptions = extractDescriptions();

  process.stderr.write("Generating markdown...\n");
  const md = generate(tiers, descriptions);

  if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, { recursive: true });
  }

  fs.writeFileSync(OUT_FILE, md, "utf-8");
  process.stderr.write(`Wrote ${OUT_FILE}\n`);
}

main();
